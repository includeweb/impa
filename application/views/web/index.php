          <div class="panel-body">
           <h4> APERTURA DE PROFESORADOS DE BIOLOGÍA, MATEMÁTICA, LENGUA Y LITERATURA E HISTORIA.</h4>
A partir de la iniciativa de los compañeros de la fábrica de Impa, y en articulación con parte del grupo fundador de la Universidad de Madres de Plaza de Mayo y la Cooperativa de Educadores e Investigadores Populares (Ceip) fue creada la Universidad de los Trabajadores (UT), con sede en Impa. La UT viene desarrollando actividades desde el año pasado, especialmente talleres de formación con temáticas de interés para las organizaciones y militantes sociales.
Para el 2013 la UT va a incorporar a sus propuestas formativas, un área de Profesorados. Esta iniciativa esta dirigida especialmente a los compañeros estudiantes graduados de los Bachilleratos Populares y a todos aquellos in
teresados en continuar sus estudios de nivel terciario. Se trata de un proyecto político pedagógico que aspira a formar, desde la perspectiva de la Educación Popular, a compañeros docentes en diferentes campos disciplinares. Los Profesorados propuestos para su apertura el año entrante son los siguientes: Biología, Matemática, Lengua y Literatura e Historia. Invitamos a todas las organizaciones sociales y a todos los los estudiantes de los Bachilleratos Populares a difundir y ser parte de esta iniciativa.
Por ello estamos organizando intercambios con todas aquellas organizaciones que expresen interés en conocer con más detalle esta propuesta que reivindica la tradición histórica de los trabajadores en organizar iniciativas educativas autogestionadas en el marco de la lucha por una educación pública y popular

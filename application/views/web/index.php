          <h3 class="panel-title">SEMINARIO DE ECONOMÍA HUMANA</h3>
        </div>
        <div class="panel-body">
          El programa del Seminario de Economía Humana se propone debatir el funcionamiento de la economía, entendiendo al conocimiento como la información procesada con criterios de pertenencia en cada realidad social específica, a ser problematizada como desafío, ya que las escuelas del pensamiento económico no tienen valor en sí mismas, sino solo contextualizando los intereses que defendían y relacionándolos con el contexto actual, principalmente cuando hoy viejas escuelas (neoclásicos, keynesianos) son resucitadas por los técnicos en economía inhumana. Será realizado en los martes de 19:50hs a las 21:50, a partir del día 13/8, en la Universidad de los Trabajadores IMPA, sobre la calle Rawson nº 106, Caballito, Capital Federal.
Cuando los mercados se ponen por encima de los gobiernos, la soberanía se ve cuestionada en sus fundamentos. El modelo neoliberal, diseñado por el Proceso y perfeccionado por Cavallo, provocó la liquidación de la democracia y del Estado como guardián del bien común y de la voluntad general. Lo estatal se separó de lo “público”. Como consecuencia, el modelo de universidad como empresa genera una “Industria Universitaria” preparando solo gestores para el mercado, teniendo como criterio la rentabilidad económica y tratando a los estudiantes como clientes. Se excluye de la educación a una parte de la juventud a partir de los criterios del mercado
La abundancia de información que nos llega diariamente descontextualizada y sin nexo entre sí, desinforma al desviar nuestro foco de atención de las cuestiones político-económicas centrales y la “información” se convierte en una muralla de acceso al conocimiento.
Las cuestiones económicas en boga se convierten en una maraña de datos, informes y recetas incomprensibles. La tecnocracia economicista entiende los asuntos vitales de la producción como tratables solo en los cielos del poder. Cerrados a la participación popular, los “econometristas” presentan sus resultados como una sabiduría “ecuménica” y elitista. Pero como decía Jauretche, cuando alguien no entiende algo de economía, no es que le falte inteligencia, sino que lo quieren embromar, y se la explican difícil para que no la pueda entender. Como la economía no es una ciencia exacta sino una ciencia social, toda la certidumbre del desarrollo es un recetario para el “asesinato económico”, del desastre y garantía de los intereses ajenos.
La Universidad de los Trabajadores IMPA es un espacio de enseñanza abierto para todos, partiendo de sus capacidades y no de los medios que cada uno dispone. No tiene clientes. Se basa en criterios de equidad y pertenencia, primero en la igualdad de acceso, y luego, en la formación de una “masa crítica”. Proponemos la universidad pública fundada en la responsabilidad social, en el concepto de universidad como servicio público recordando la “reforma universitaria de 1918”, y tomando a la educación como eje de la democratización en la Patria Grande. Nuestro propósito es eliminar el lucro y los criterios de rentabilidad económica en la gestión académica. La Universidad de los Trabajadores IMPA propone la autogestión del trabajo y de la vida, para defender las conquistas populares, como respuesta a las crisis y a los males de la competencia (mercado) como reguladora de la sociedad, evidenciando la inexistencia de soluciones dentro del capitalismo.
